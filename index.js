function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b){
  return a * b;
}

function divide(a, b){
  return a / b;
}

function testAdd() {
  console.log(add(1, 2) === 2);
}

function testSubtract() {
  console.log(subtract(3, 1) === 2);
}

function testMultiply() {
  console.log(multiply(2, 3) === 6);
}

function testDivide() {
  console.log(divide(3, 3) === 1);
}

